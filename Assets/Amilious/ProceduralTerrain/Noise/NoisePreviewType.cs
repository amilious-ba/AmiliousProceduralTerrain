namespace Amilious.ProceduralTerrain.Noise {
    
    /// <summary>
    /// This enum is used to indicate which preview type
    /// should be displayed.
    /// </summary>
    public enum NoisePreviewType {
        
        /// <summary>
        /// This option will display the noise in black and white.
        /// </summary>
        NoiseMap,
        
        /// <summary>
        /// This option will display the noise in color.
        /// </summary>
        ColorMap, 
        
        /// <summary>
        /// This option will let you view the noise from the side.
        /// </summary>
        CrossSection
    }
}